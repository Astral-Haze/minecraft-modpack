function chest(ChestNamespace, chestAssID, chestID, chestLogs, chestPlanks) {
event.recipes.gtceu.assembler('astral:assembler_' + ChestNamespace + '_' + chestAssID + '_chest')
  .itemInputs(
    '8x ' + chestPlanks
  )
  .itemOutputs(
    chestID
  )
  .duration(100)
  .EUt(4)
  .circuit(8)

event.shaped(
  Item.of(chestID, 1),
  [
    'CBC',
    'BAB',
    'CBC'
  ],
  {
    A: 'minecraft:flint',
    B: chestLogs,
    C: chestPlanks
  }
)


}
